import { test } from 'qunit';
import moduleForAcceptance from 'open-event-frontend/tests/helpers/module-for-acceptance';

moduleForAcceptance('Acceptance | public/sessions');

test('visiting / and opening an event and going to sessions', function(assert) {
  visit('/');
  andThen(function() {
    click('div.event.card:first > a.content:first');
    andThen(function() {
      assert.equal(currentRouteName(), 'public.index');
      click('div.ui.vertical.pointing.menu > a:contains(\'Sessions\')');
      andThen(function() {
        assert.equal(currentRouteName(), 'public.sessions.index');
      });
    });
  });
});
